package com.xiongtao.jetpack.databinding.java;

public class Star4 {
    private String name;
    private int fans;

    public Star4(String name, int fans) {
        this.name = name;
        this.fans = fans;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFans() {
        return fans;
    }

    public void setFans(int fans) {
        this.fans = fans;
    }
}
